function isRetryableGeminiError(error: unknown) {
  const message = (error as Error)?.message ?? ''
  const status =
    (error as any)?.status ??
    (error as any)?.response?.status ??
    (error as any)?.code

  return (
    status === 408 ||
    status === 503 ||
    status === 504 ||
    status === 524 ||
    (error as Error)?.name === 'AbortError' ||
    /DEADLINE_EXCEEDED/i.test(String(message)) ||
    /UNAVAILABLE/i.test(String(message)) ||
    /model is overloaded/i.test(String(message)) ||
    /timed out/i.test(String(message)) ||
    /timeout/i.test(String(message)) ||
    /aborted/i.test(String(message)) ||
    /error code: 524/i.test(String(message))
  )
}

function isInvalidGeminiApiKeyError(error: unknown) {
  const message = (error as Error)?.message ?? ''
  const details = (error as any)?.errorDetails

  return (
    /API key not valid/i.test(String(message)) ||
    /API_KEY_INVALID/i.test(String(message)) ||
    (Array.isArray(details) &&
      details.some(
        (detail) =>
          detail?.reason === 'API_KEY_INVALID' ||
          /API key not valid/i.test(String(detail?.message ?? ''))
      ))
  )
}

async function withAbortTimeout<T>(
  timeoutMs: number,
  fn: (abortSignal: AbortSignal) => Promise<T>
) {
  const controller = new AbortController()
  const timer = setTimeout(() => controller.abort(), timeoutMs)

  try {
    return await fn(controller.signal)
  } finally {
    clearTimeout(timer)
  }
}

async function waitForGeminiFileActive(
  ctx: Pick<TrpcContext, 'googleGenAIFileManager'>,
  fileName: string,
  abortSignal: AbortSignal
) {
  while (true) {
    if (abortSignal.aborted) {
      throw new Error('Gemini file activation timed out')
    }

    const file = await ctx.googleGenAIFileManager.getFile(fileName)

    if (file.state === 'ACTIVE') {
      return file
    }

    if (file.state === 'FAILED') {
      throw new Error('Gemini failed to process uploaded PDF')
    }

    await new Promise((resolve) => setTimeout(resolve, 1000))
  }
}

export const receivedInvoicesRouter = trpcContext.router({
  list: protectedProc
    .input(
      z.object({
        from: stringDateSchema.optional().nullable(),
        to: stringDateSchema.optional().nullable()
      })
    )
    .query(async ({ ctx, input }) => {
      const conditions: SQL[] = [eq(receivedInvoiceTb.user_id, ctx.user.id)]

      if (input.from) {
        conditions.push(gte(receivedInvoiceTb.taxable_supply_date, input.from))
      }
      if (input.to) {
        conditions.push(lt(receivedInvoiceTb.taxable_supply_date, input.to))
      }

      const invoices = await ctx.db
        .select()
        .from(receivedInvoiceTb)
        .where(and(...conditions))
        .orderBy(desc(receivedInvoiceTb.taxable_supply_date))
      return invoices
    }),

  getById: protectedProc
    .input(z.object({ id: z.string() }))
    .query(async ({ ctx, input }) => {
      const invoice = await ctx.db
        .select()
        .from(receivedInvoiceTb)
        .where(
          and(
            eq(receivedInvoiceTb.id, input.id),
            eq(receivedInvoiceTb.user_id, ctx.user.id)
          )
        )
        .limit(1)

      if (!invoice.length) {
        throw new TRPCError({
          code: 'NOT_FOUND',
          message: 'Invoice not found'
        })
      }

      return invoice[0]
    }),

  create: protectedProc
    .input(receivedInvoiceCreateSchema)
    .mutation(async ({ ctx, input }) => {
      const { attachment_data: _attachment_data, ...invoiceData } = input

      // Basic check for duplicate invoice for the same supplier/number
      const existing = await ctx.db
        .select({ id: receivedInvoiceTb.id })
        .from(receivedInvoiceTb)
        .where(
          and(
            eq(receivedInvoiceTb.user_id, ctx.user.id),
            eq(receivedInvoiceTb.supplier_name, invoiceData.supplier_name),
            eq(receivedInvoiceTb.invoice_number, invoiceData.invoice_number)
          )
        )
        .limit(1)

      if (existing.length > 0) {
        throw new TRPCError({
          code: 'CONFLICT',
          message:
            'An invoice with this number from this supplier already exists.'
        })
      }

      // Save the invoice
      const [newInvoice] = await ctx.db
        .insert(receivedInvoiceTb)

        .values({
          ...invoiceData,
          user_id: ctx.user.id,
          items: invoiceData.items ?? [],
          tags: invoiceData.tags ?? []
        })
        .returning()

      return newInvoice
    }),

  update: protectedProc
    .input(
      receivedInvoiceCreateSchema.extend({
        id: z.string()
      })
    )
    .mutation(async ({ ctx, input }) => {
      const { id, attachment_data: _attachment_data, ...updateData } = input

      // Check if invoice exists and belongs to user
      const existing = await ctx.db
        .select({ id: receivedInvoiceTb.id })
        .from(receivedInvoiceTb)
        .where(
          and(
            eq(receivedInvoiceTb.id, id),
            eq(receivedInvoiceTb.user_id, ctx.user.id)
          )
        )
        .limit(1)

      if (!existing.length) {
        throw new TRPCError({
          code: 'NOT_FOUND',
          message: 'Invoice not found or you do not have permission to edit it.'
        })
      }

      // Update the invoice
      const [updatedInvoice] = await ctx.db
        .update(receivedInvoiceTb)
        .set({
          ...updateData,
          updated_at: new Date().toISOString()
        })
        .where(eq(receivedInvoiceTb.id, id))
        .returning()

      return updatedInvoice
    }),

  delete: protectedProc
    .input(z.object({ id: z.string() }))
    .mutation(async ({ ctx, input }) => {
      // Check if invoice exists and belongs to user
      const existing = await ctx.db
        .select({ id: receivedInvoiceTb.id })
        .from(receivedInvoiceTb)
        .where(
          and(
            eq(receivedInvoiceTb.id, input.id),
            eq(receivedInvoiceTb.user_id, ctx.user.id)
          )
        )
        .limit(1)

      if (!existing.length) {
        throw new TRPCError({
          code: 'NOT_FOUND',
          message:
            'Invoice not found or you do not have permission to delete it.'
        })
      }

      // Delete the invoice
      await ctx.db
        .delete(receivedInvoiceTb)
        .where(eq(receivedInvoiceTb.id, input.id))

      return { success: true }
    }),

  // OCR an invoice with Gemini API
  extractInvoiceData: protectedProc
    .input(
      z.object({
        mimeType: z.string(),
        imageData: z.string() // Base64 encoded image data
      })
    )
    .mutation(async ({ ctx, input }) => {
      try {
        if (!schema) {
          throw new TRPCError({
            code: 'INTERNAL_SERVER_ERROR',
            message: 'Failed to load schema for OCR processing'
          })
        }

        // Clean the base64 data (remove any data URI prefix, e.g. data:image/jpeg;base64, or data:application/pdf;base64,)
        const base64Data = input.imageData.replace(
          /^data:[^;]+;base64,/,
          ''
        )
        // Get the model

        // TODO use https://googleapis.github.io/js-genai/main/classes/files.Files.html to allow uploading files bigger than 7MB

        const requestPlan =
          input.mimeType === 'application/pdf'
            ? [
                {
                  model: 'gemini-3-flash-preview',
                  timeoutMs: PDF_FULL_ATTEMPT_TIMEOUT_MS,
                  prompt: fullExtractionPrompt,
                  responseSchema: schema as any,
                  maxOutputTokens: 2048
                },
                {
                  model: 'gemini-3-flash-preview',
                  timeoutMs: PDF_FALLBACK_ATTEMPT_TIMEOUT_MS,
                  prompt: pdfFallbackPrompt,
                  responseSchema: pdfFallbackSchema as any,
                  maxOutputTokens: 768
                }
              ]
            : [
                {
                  model: 'gemini-3-flash-preview',
                  timeoutMs: GEMINI_REQUEST_TIMEOUT_MS,
                  prompt: fullExtractionPrompt,
                  responseSchema: schema as any,
                  maxOutputTokens: 2048
                }
              ]

        const totalTimeoutMs =
          input.mimeType === 'application/pdf'
            ? PDF_TOTAL_REQUEST_TIMEOUT_MS
            : GEMINI_REQUEST_TIMEOUT_MS

        const result = await withAbortTimeout(totalTimeoutMs, async (
          abortSignal
        ) => {
          let uploadedFileName: string | undefined
          let lastError: unknown

          try {
            const filePart =
              input.mimeType === 'application/pdf'
                ? await (async () => {
                    const uploadedFile =
                      await ctx.googleGenAIFileManager.uploadFile(
                        Buffer.from(base64Data, 'base64'),
                        {
                          mimeType: input.mimeType,
                          displayName: 'invoice.pdf'
                        }
                      )

                    uploadedFileName = uploadedFile.file.name

                    const activeFile =
                      uploadedFile.file.name &&
                      uploadedFile.file.state !== 'ACTIVE'
                        ? await waitForGeminiFileActive(
                            ctx,
                            uploadedFile.file.name,
                            abortSignal
                          )
                        : uploadedFile.file

                    return {
                      fileData: {
                        fileUri: activeFile.uri,
                        mimeType: activeFile.mimeType ?? input.mimeType
                      }
                    }
                  })()
                : {
                    inlineData: {
                      mimeType: input.mimeType,
                      data: base64Data
                    }
                  }

            for (const attempt of requestPlan) {
              try {
                return await ctx.googleGenAI.models.generateContent({
                  model: attempt.model,
                  contents: [
                    {
                      parts: [{ text: attempt.prompt }, filePart]
                    }
                  ],
                  config: {
                    httpOptions: {
                      timeout: attempt.timeoutMs
                    },
                    abortSignal,
                    systemInstruction: `You are a data extraction assistant. Your main objective is to extract invoice data from a czech invoice document. Most likely the invoice is in CZK currency, but on the invoice it is often represented as Kč. The format we want for currency field is ISO 4217.`,
                    responseMimeType: 'application/json',
                    responseSchema: attempt.responseSchema,
                    maxOutputTokens: attempt.maxOutputTokens,
                    temperature: 0.2,
                    thinkingConfig: {
                      includeThoughts: false,
                      thinkingBudget: 0
                    }
                  }
                })
              } catch (err) {
                lastError = err
                if (!isRetryableGeminiError(err)) {
                  throw err
                }
              }
            }

            throw lastError
          } finally {
            if (uploadedFileName) {
              void ctx.googleGenAIFileManager
                .deleteFile(uploadedFileName)
                .catch((error) => {
                  console.error('Failed to delete uploaded Gemini file:', error)
                })
            }
          }
        })

        const textContent = result.text?.trim() ?? ''

        // Try to find JSON object in the response
        let extractedData
        try {
          extractedData = JSON.parse(textContent)
        } catch (error) {
          try {
            const jsonMatch = textContent.match(/\{[\s\S]*\}/)
            if (jsonMatch) {
              extractedData = JSON.parse(jsonMatch[0])
            } else {
              throw new Error('No valid JSON found in response')
            }
          } catch (fallbackError) {
            console.error('Error parsing Gemini response:', error, fallbackError)
            throw new TRPCError({
              code: 'INTERNAL_SERVER_ERROR',
              message: 'Failed to parse OCR results'
            })
          }
        }

        console.log('extractedData for invoice:', extractedData)
        // Validate data against our schema
        try {
          const validatedData = ocrResponseSchema.parse(extractedData)
          return validatedData
        } catch (error) {
          console.error('Validation error:', error)
          throw new TRPCError({
            code: 'INTERNAL_SERVER_ERROR',
            message: 'OCR results did not match expected format'
          })
        }
      } catch (error) {
        console.error('OCR processing error:', error)
        if (error instanceof TRPCError) {
          throw error
        }
        if (isInvalidGeminiApiKeyError(error)) {
          throw new TRPCError({
            code: 'INTERNAL_SERVER_ERROR',
            message:
              'Gemini API key is invalid in backend configuration. Update GEMINI_API_KEY.'
          })
        }
        throw new TRPCError({
          code: 'INTERNAL_SERVER_ERROR',
          message: 'Failed to process image: ' + (error as Error).message
        })
      }
    })
})

